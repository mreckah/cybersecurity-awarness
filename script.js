// === State & Init ===
let currentLang = localStorage.getItem('appLang') || 'en';
let currentTheme = localStorage.getItem('appTheme') || 'dark';

document.addEventListener('DOMContentLoaded', () => {
  initTheme();
  initLang();
  initChecklist();
  initQuiz();
  initScrollSpy();
  initAnimations();
  initMobileMenu();
});

// === Language Handling ===
function initLang() {
  updateLanguageTexts(currentLang);
  const langToggle = document.getElementById('lang-toggle');
  const currentLangSpan = document.getElementById('current-lang');
  
  currentLangSpan.textContent = currentLang.toUpperCase();
  
  langToggle.addEventListener('click', () => {
    currentLang = currentLang === 'en' ? 'fr' : 'en';
    localStorage.setItem('appLang', currentLang);
    currentLangSpan.textContent = currentLang.toUpperCase();
    updateLanguageTexts(currentLang);
    initQuiz(); // Re-render quiz in the new language
  });
}

function updateLanguageTexts(lang) {
  const elements = document.querySelectorAll('[data-i18n]');
  elements.forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
}

// === Theme Handling ===
function initTheme() {
  const body = document.body;
  const themeToggle = document.getElementById('theme-toggle');
  const themeIcon = themeToggle.querySelector('i');

  if (currentTheme === 'light') {
    body.classList.remove('dark-mode');
    themeIcon.classList.replace('fa-moon', 'fa-sun');
  } else {
    body.classList.add('dark-mode');
    themeIcon.classList.replace('fa-sun', 'fa-moon');
  }

  themeToggle.addEventListener('click', () => {
    body.classList.toggle('dark-mode');
    const isDark = body.classList.contains('dark-mode');
    
    if (isDark) {
      currentTheme = 'dark';
      themeIcon.classList.replace('fa-sun', 'fa-moon');
    } else {
      currentTheme = 'light';
      themeIcon.classList.replace('fa-moon', 'fa-sun');
    }
    localStorage.setItem('appTheme', currentTheme);
  });
}

// === Checklist Handling ===
function initChecklist() {
  const items = document.querySelectorAll('.check-item');
  const progressBar = document.getElementById('progress-bar');
  const progressCount = document.getElementById('progress-count');
  
  let completed = 0;
  const total = items.length;

  items.forEach(item => {
    item.addEventListener('click', function() {
      this.classList.toggle('checked');
      
      const isChecked = this.classList.contains('checked');
      const icon = this.querySelector('i');
      
      if (isChecked) {
        completed++;
      } else {
        completed--;
      }
      
      updateProgress();
    });
  });

  function updateProgress() {
    progressCount.textContent = completed;
    const percentage = (completed / total) * 100;
    progressBar.style.width = percentage + '%';
  }
}

// === Quiz Object ===
const quizQuestions = [
  {
    q: "quiz.q1",
    options: ["quiz.q1.o1", "quiz.q1.o2", "quiz.q1.o3"],
    correctIndex: 1
  },
  {
    q: "quiz.q2",
    options: ["quiz.q2.o1", "quiz.q2.o2", "quiz.q2.o3"],
    correctIndex: 0
  },
  {
    q: "quiz.q3",
    options: ["quiz.q3.o1", "quiz.q3.o2", "quiz.q3.o3"],
    correctIndex: 1
  }
];

let currentQuestion = 0;
let score = 0;

function initQuiz() {
  currentQuestion = 0;
  score = 0;
  renderQuestion(currentQuestion);
}

function renderQuestion(index) {
  const container = document.getElementById('quiz-container');
  const dict = translations[currentLang];
  
  if (index >= quizQuestions.length) {
    // Show Results
    container.innerHTML = `
      <div class="result-display text-center" style="padding: 2rem;">
        <i class="fa-solid fa-trophy text-safe" style="font-size: 3rem; margin-bottom: 1rem;"></i>
        <h3 style="font-size: 1.5rem; margin-bottom: 0.5rem">${dict["quiz.result.score"]} ${score} / ${quizQuestions.length}</h3>
        <button id="restart-quiz" class="btn btn-primary" style="margin-top: 1rem">${dict["quiz.btn.restart"]}</button>
      </div>
    `;
    document.getElementById('restart-quiz').addEventListener('click', initQuiz);
    return;
  }

  const qData = quizQuestions[index];
  
  let optionsHtml = '';
  qData.options.forEach((optKey, i) => {
    optionsHtml += `<button class="option-btn" data-index="${i}">${dict[optKey]}</button>`;
  });

  container.innerHTML = `
    <div class="quiz-header">
      <span>Question ${index + 1} / ${quizQuestions.length}</span>
    </div>
    <div class="question-text">${dict[qData.q]}</div>
    <div class="options-grid">
      ${optionsHtml}
    </div>
    <button id="next-btn" class="btn btn-outline btn-next">${dict["quiz.btn.next"]}</button>
  `;

  const optionBtns = container.querySelectorAll('.option-btn');
  const nextBtn = document.getElementById('next-btn');

  optionBtns.forEach(btn => {
    btn.addEventListener('click', function() {
      // Disable all buttons
      optionBtns.forEach(b => b.disabled = true);
      
      const selectedIndex = parseInt(this.getAttribute('data-index'));
      
      if (selectedIndex === qData.correctIndex) {
        this.classList.add('correct');
        score++;
      } else {
        this.classList.add('incorrect');
        // Highlight correct logic
        optionBtns[qData.correctIndex].classList.add('correct');
      }
      
      nextBtn.style.display = 'inline-block';
    });
  });

  nextBtn.addEventListener('click', () => {
    currentQuestion++;
    renderQuestion(currentQuestion);
  });
}

// === Scroll Spy ===
function initScrollSpy() {
  const sections = document.querySelectorAll('.section, .hero');
  const navItems = document.querySelectorAll('.nav-item');

  window.addEventListener('scroll', () => {
    let current = '';
    const scrollY = window.pageYOffset;

    sections.forEach(section => {
      const sectionTop = section.offsetTop - 100;
      const sectionHeight = section.offsetHeight;
      
      if (scrollY >= sectionTop && scrollY < sectionTop + sectionHeight) {
        current = section.getAttribute('id');
      }
    });

    navItems.forEach(item => {
      item.classList.remove('active');
      if (item.getAttribute('href') === '#' + current) {
        item.classList.add('active');
      }
    });
  });
}

// === Intersection Observer for Animations ===
function initAnimations() {
  const hiddenElements = document.querySelectorAll('.fade-in-section');
  
  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting) {
        entry.target.classList.add('is-visible');
        observer.unobserve(entry.target); // Run once
      }
    });
  }, { threshold: 0.1 });

  hiddenElements.forEach(el => observer.observe(el));
}

// === Mobile Menu ===
function initMobileMenu() {
  const mobileBtn = document.getElementById('mobile-menu-btn');
  const navLinks = document.querySelector('.nav-links');
  const navItems = document.querySelectorAll('.nav-item');

  mobileBtn.addEventListener('click', () => {
    navLinks.classList.toggle('show-mobile');
    const icon = mobileBtn.querySelector('i');
    if (navLinks.classList.contains('show-mobile')) {
      icon.classList.replace('fa-bars', 'fa-xmark');
    } else {
      icon.classList.replace('fa-xmark', 'fa-bars');
    }
  });

  // Close menu on link click
  navItems.forEach(item => {
    item.addEventListener('click', () => {
      if (window.innerWidth <= 992) {
        navLinks.classList.remove('show-mobile');
        mobileBtn.querySelector('i').classList.replace('fa-xmark', 'fa-bars');
      }
    });
  });
}
